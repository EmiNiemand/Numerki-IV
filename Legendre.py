def legendreStart(function, l_range: float, u_range: float, n: int):
    XNodes = [[-(1 / 3) ** .5, ((1 / 3) ** .5)],
              [-(3 / 5) ** .5, 0, ((3 / 5) ** .5)],
              [-((3 / 7 + 2 / 7 * (6 / 5) ** .5) ** .5), -((3 / 7 - 2 / 7 * (6 / 5) ** .5) ** .5),
               (3 / 7 - 2 / 7 * (6 / 5) ** .5) ** .5, (3 / 7 + 2 / 7 * (6 / 5) ** .5) ** .5],
              [-(((5 + 2 * (10 / 7) ** .5) ** .5) / 3), -(((5 - 2 * (10 / 7) ** .5) ** .5) / 3), 0,
               ((5 - 2 * (10 / 7) ** .5) ** .5) / 3, ((5 + 2 * (10 / 7) ** .5) ** .5) / 3],
              [-0.932469514203152027812, -0.661209386466264513661, -0.238619186083196908631,
               0.238619186083196908631, 0.661209386466264513661, 0.932469514203152027812],
              [-0.949107912342758524526, -0.741531185599394439864, -0.405845151377397166907, 0,
               0.405845151377397166907, 0.741531185599394439864, 0.949107912342758524526]]
    AkFactors = [[1, 1],
               [5 / 9, 8 / 9, 5 / 9],
               [(18 - 30 ** .5) / 36, (18 + 30 ** .5) / 36, (18 + 30 ** .5) / 36, (18 - 30 ** .5) / 36],
               [(322 - 13 * 70 ** .5) / 900, (322 + 13 * 70 ** .5) / 900, 128 / 225,
                (322 + 13 * 70 ** .5) / 900, (322 - 13 * 70 ** .5) / 900],
                 [0.171324492379170345040, 0.360761573048138607570, 0.467913934572691047390,
                  0.467913934572691047390, 0.360761573048138607570, 0.171324492379170345040],
                 [0.129484966168869693271, 0.279705391489276667901, 0.381830050505118944950,
                  0.417959183673469387755, 0.381830050505118944950, 0.279705391489276667901,
                  0.129484966168869693271]]

    result = 0
    for i in range(n):
        # https://ftims.edu.p.lodz.pl/pluginfile.php/164431/mod_resource/content/2/Fortuna%20Macukow%20Wasowski.pdf
        # str. 179
        t = ((l_range + u_range) / 2) + ((u_range - l_range) / 2) * XNodes[n - 2][i]
        result += AkFactors[n - 2][i] * function(t)

    return result * ((u_range - l_range) / 2)
